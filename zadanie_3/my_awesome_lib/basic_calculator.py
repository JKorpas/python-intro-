class BasicCalculator:
    """Podstawowe operacje matematyczne"""

    # Dodawanie
    def add(self, x, y):
        return x + y

    # Odejmowanie
    def sub(self, x, y):
        return x - y

    # Mno≈ºenie
    def mul(self, x, y):
        return x * y

    # Dzielenie
    def div(self, x, y):
        return x / y
